#!/usr/bin/env node
// @ts-check
// How Much AI device-pairing helper.
//
// Reads the Claude Code OAuth token already stored on THIS machine and sends it to the app to pair a
// device shown a code in the browser. Uses ONLY Node built-ins (+ the dependency-free credential
// reader shared with the web app) so `npx` starts instantly with no install.
//
// Usage:  npx github:SeraphKc/how-much-ai#v0.1.0 connect ABCD-EFGH-JKLM
//         HOW_MUCH_AI_URL=https://your-deploy.example npx github:SeraphKc/how-much-ai#v0.1.0 connect <code>
//
// Flags:  -y / --yes   skip the confirmation prompt (for scripting)

import process from "node:process";
import readline from "node:readline";
import { readLocalCredentialRaw, extractTokens, LocalCredentialError } from "../lib/credential-source.mjs";
import { CONNECT_TIMEOUT_MS, parseHmcUrl, serverErrorText } from "../lib/connect-url.mjs";

function ask(question) {
  return new Promise((resolve) => {
    const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
    rl.question(question, (answer) => {
      rl.close();
      resolve(String(answer).trim());
    });
  });
}

async function main() {
  let baseUrl;
  try {
    // HMC_URL remains a compatibility alias for commands generated by older self-hosted releases.
    baseUrl = parseHmcUrl(process.env.HOW_MUCH_AI_URL || process.env.HMC_URL);
  } catch (err) {
    console.error(`✗ ${err instanceof Error ? err.message : "Invalid pairing target."}`);
    process.exitCode = 1;
    return;
  }

  const args = process.argv.slice(2);
  const yes = args.includes("-y") || args.includes("--yes");
  const positional = args.filter((a) => a !== "-y" && a !== "--yes");
  // Accept "connect <code>" or a bare "<code>".
  const code = positional[0] === "connect" ? positional[1] : positional[0];

  if (!code) {
    console.error("Usage: connect <code>\n  Get the code from the \"Connect account\" dialog at " + baseUrl + ".");
    process.exitCode = 1;
    return;
  }

  // Plain-English notice + consent, so nothing happens silently.
  console.log(
    [
      "",
      "How Much AI — connect this device",
      "",
      "This reads the Claude Code login token already on THIS machine and sends it ONLY to",
      `  ${baseUrl}`,
      "to pair this device with the code you were shown. The token is sent over an encrypted",
      "HTTPS connection (except an explicitly selected loopback HTTP development server), stored",
      "encrypted, and this helper is open source.",
      "",
      "Quick-connect shares Claude Code's rotating login. For always-on monitoring, return",
      "to the dashboard and choose its private app login instead.",
      "That creates an isolated renewable session without sharing this CLI credential.",
      "",
    ].join("\n"),
  );

  if (!yes) {
    const answer = await ask("Continue? [y/N] ");
    if (!/^y(es)?$/i.test(answer)) {
      console.log("Aborted — nothing was sent.");
      return;
    }
  }

  // Read the local credential (macOS Keychain via execFile, else ~/.claude/.credentials.json).
  let tokens;
  try {
    const raw = await readLocalCredentialRaw();
    tokens = extractTokens(raw);
  } catch (err) {
    if (err instanceof LocalCredentialError) {
      console.error(`\n✗ ${err.message}\n  ${err.recommendation}`);
    } else {
      console.error(`\n✗ Couldn't read the local Claude Code credential: ${err instanceof Error ? err.message : err}`);
    }
    process.exitCode = 1;
    return;
  }

  // Send it. Never print the token.
  let res;
  try {
    res = await fetch(`${baseUrl}/api/connect/pair/complete`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ code, token: tokens }),
      signal: AbortSignal.timeout(CONNECT_TIMEOUT_MS),
    });
  } catch (err) {
    const timedOut = err instanceof Error && (err.name === "TimeoutError" || err.name === "AbortError");
    console.error(
      timedOut
        ? `\n✗ Connection to ${baseUrl} timed out after ${CONNECT_TIMEOUT_MS / 1000} seconds.`
        : `\n✗ Couldn't reach ${baseUrl}: ${err instanceof Error ? err.message : err}`,
    );
    process.exitCode = 1;
    return;
  }

  const data = await res.json().catch(() => ({}));
  if (res.ok && data && data.ok) {
    console.log(`\n✓ Connected ${data.email || "your account"}. You can close this window — the dashboard will update.`);
  } else {
    console.error(
      `\n✗ ${serverErrorText(data && data.error, `Pairing failed (HTTP ${res.status}).`, data && data.errorId)}`,
    );
    process.exitCode = 1;
  }
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : err);
  process.exitCode = 1;
});
